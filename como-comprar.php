<section id="como-comprar" class="institucional">
    <div class="container">
        <p>
            Para comprar em nossa loja é muito fácil:
            <br><br>
            1º Passo: Escolhendo os produtos
            <br><br>
            NAVEGAÇÃO<br>
            Se você estiver navegando pelo site e quiser encontrar um produto, basta clicar na categoria desejada, localizada no topo do site e em seguida navegar pelas páginas exibidas.
            <br><br>
            BUSCA<br>
            Caso você queira procurar por algum produto específico, basta digitar o nome do produto na barra de busca, localizada no topo do site.
            <br><br>
            2º Passo: Carrinho de compras
            <br><br>
            Ao escolher o produto, basta clicar no botão “Adicionar no Carrinho” ou “Comprar” e seu produto será adicionado a seu carrinho de compras, localizado no topo do site
            <br><br>
            Clicando no ícone do carrinho, você será direcionado para uma página onde poderá visualizar todos os itens que adicionou, poderá fazer a simulação do frete para saber o valor e o prazo de entrega, incluir um código de desconto, caso tenha, lterar a quantidade de produtos a lista ou remover um produto que não deseja mais comprar.
            <br><br>
            3º Passo: Identificação<br>
            Ao definir a sua lista de compras, basta clicar em "Finalizar Compra" e será encaminhado para a página de identificação. Caso você já tenha cadastro em nosso site, basta inserir seu e-mail ou CPF e sua senha para prosseguir. Mas caso ainda seja cadastrado, basta clicar em “Criar Cadastro” e preencher um formulário com algumas informações.
            Se você já é cadastrado, mas esqueceu sua senha basta clicar no link "Esqueci minha senha" que está embaixo do campo de e-mail. OBS.: As informações fornecidas são de uso exclusivo para realização de análise de pedidos, emissão de notas fiscais e para contato com o cliente, e não são fornecidos a terceiros.
            <br><br>
            4º Passo: Escolha o endereço<br>
            Após se identificar, você deverá confirmar o endereço de entrega dos produtos. O nosso site permite que você cadastre mais de um endereço de entrega para seus pedidos.
            O endereço do seu cadastro é definido como padrão para entrega, mas você pode alterá-lo a qualquer momento clicando em "Alterar Endereço", onde poderá cadastrar outros endereços de entrega, como por exemplo o endereço do seu trabalho, casa da mãe, etc. Você também poderá editar um endereço já existente ou excluí-lo.
            <br><br>
            5º Passo: Pagamento<br>
            Você verá um breve resumo de seu pedido, com os produtos, valor total da compra e o endereço de entrega já escolhido. Agora basta definir a forma de pagamento que deseja finalizar seu pedido.
        </p>
    </div>
</section>

